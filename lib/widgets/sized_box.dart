import 'package:flutter/material.dart';

class QuoteSizedBox {
  SizedBox settingsSizedBox(
    double height,
  ) {
    return SizedBox(
      height: height,
    );
  }

  SizedBox quotesSizedBox(double height, double width) {
    return SizedBox(
      height: height,
      width: width,
    );
  }

  SizedBox addQuoteSizedBox(
    double height,
  ) {
    return SizedBox(
      height: height,
    );
  }
}
